const FALLBACK = G['?'];

export function glyphFor(ch: string): string[] {
  return G[ch] ?? G[ch.toUpperCase()] ?? FALLBACK;
}

export function glyphWidth(ch: string): number {
  return glyphFor(ch)[0].length;
}

export function measureText(str: string, scale = 1): number {
  let best = 0;
  for (const line of str.split('\n')) {
    let w = 0;
    for (const ch of line) w += (glyphWidth(ch) + 1) * scale;
    best = Math.max(best, Math.max(0, w - scale));
  }
  return best;
}

/** Greedy word-wrap to a pixel width. */
export function wrapText(str: string, maxWidth: number, scale = 1): string[] {
  const out: string[] = [];
  for (const para of str.split('\n')) {
    const words = para.split(' ');
    let line = '';
    for (const word of words) {
      const probe = line ? line + ' ' + word : word;
      if (measureText(probe, scale) <= maxWidth || !line) {
        line = probe;
      } else {
        out.push(line);
        line = word;
      }
    }
    out.push(line);
  }
  return out;
}
